    private void listaColabMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listaColabMouseClicked
        // TODO add your handling code here:
        int i = listaColab.getSelectedIndex();
        colaboracion c = null;
        c = ic.listarColaboraciones().get(i);
        
        textoNickname.setText(c.getColaborador().getNickname());
        textoHora.setText(c.getFecha().toString());
        textoMonto.setText(c.getMontoAportado().toString());
        textoTipo.setText(c.getTipoRetorno());
        
        this.colab = c;
        
    }//GEN-LAST:event_listaColabMouseClicked

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        // TODO add your handling code here:
        
        //elimino la colab
        if(colab != null){
        int id = colab.getId();
        ic.eliminoColab(id);
        }else{
        javax.swing.JOptionPane.showMessageDialog(this, "Selecciona colaboracion", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_botonEliminarActionPerformed


